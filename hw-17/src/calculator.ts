export class Calculator {
    constructor() {}
     
     public sum(a: any, b: any): any {
        return a + b;
    }
     public diff(a: number, b: number): number {
        return a - b;
    }
     public multpl(a: number, b: number): any {
        return a * b;
    }
     public div(a: number, b: number): number {
        return a / b;
    }
  }
  